int trace_ray(
	real_t origin[], real_t direction[], 
	real_t (*sdef)(real_t x, real_t y, real_t z),
	real_t* t, real_t maxt,
	real_t eps,
	real_t* lasth
)
{
	int i;

	*t = 0.0f;

	for(i=0; i<1024; ++i)
	{
		real_t point[3] = {
			origin[0] + *t*direction[0],
			origin[1] + *t*direction[1],
			origin[2] + *t*direction[2]
		};

		real_t h = ABS(
			sdef(point[0], point[1], point[2])
		);

		*lasth = h;

		if (h<eps)
			return 0;

		if (*t>maxt)
			return 2;

		*t += h;
	}

	return 1;
}

